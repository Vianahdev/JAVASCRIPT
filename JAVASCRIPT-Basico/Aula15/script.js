/* FOR */
var v_numero = 5;

for(var i = 0; i < v_numero; i++){
    console.log("Executado a estrutura de repetição pela " + i);
}

console.log("Acabou");


/* WHILE */
var v_numero = 0;

while( v_numero < 10){
    console.log("Executado a estrutura de repetição pela " + v_numero);
    v_numero++;
}
console.log("Acabou");
