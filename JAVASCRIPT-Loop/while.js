let x = 0

//Estrutura de repetição While
while (x <= 10) {
  document.write(x + "<br/>")
  x++ //Incremento de um valor na variavel
}

// Utilizando o break para encerrar o laço de retição
let x = 0

//Estrutura de repetição While
while (x <= 10) {
  document.write(x + "<br/>")

  if (x == 5) {
    break
  }
  x++ //Incremento de um valor na variavel
}